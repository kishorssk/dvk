					<p class="title">ಆಹಾರದಿಂದ ಆರೋಗ್ಯ</p>
					<p class="edition">೪ನೇ ಆವೃತ್ತಿ ⁘ ೬೪ ಪುಟಗಳು</p>
					<p class="description">
						ಆಹಾರದ ಬಗ್ಗೆ ಎಲ್ಲ ಉಪಯುಕ್ತ ಮಾಹಿತಿ ಒಂದೆಡೆ ದೊರೆಯಬೇಕು ಎನ್ನುವ ನನ್ನ ಅಪೇಕ್ಷೆಯೇ ಈ ಕೃತಿಯ ರಚನೆಗೆ ಪ್ರೇರಣೆ. ಇಪ್ಪತ್ತೊಂದನೆಯ ಶತಮಾನಕ್ಕೆ ಕಾಲಿಡುತ್ತಿರುವ ನಾವು, ಪರಿಸರ ಮಾಲಿನ್ಯದ ಜೊತೆಗೆ ಆಹಾರ ಮಾಲಿನ್ಯವನ್ನೂ ಎದುರಿಸಬೇಕಾಗಿದೆ. ಪ್ರಕೃತಿಯಲ್ಲಿ ದೊರೆಯುವ ಯಥೇಚ್ಛ ಆಹಾರವನ್ನು ಅದರ ತಾಜಾತನದೊಂದಿಗೆ ಬಳಸಿಕೊಳ್ಳಲು ಶಕ್ತರಾದರೆ, ನಮ್ಮ ಆರೋಗ್ಯ ವೃದ್ಧಿಸುವುದರಲ್ಲಿ ಯಾವ ಸಂಶಯವೂ ಇಲ್ಲ. ನಾವು ದಿನ ನಿತ್ಯ ಬಳಸುವ ಆಹಾರಗಳಲ್ಲಿ ಯಾವ ಯಾವ ಪೌಷ್ಟಿಕಾಂಶಗಳಿವೆ ? ಅವು ಜೀವ ಕೋಟಿಗೆ ಯಾವ ರೀತಿ ನೆರವಾಗುತ್ತವೆ? ಎಂಬ ವಿವರಗಳು ಸಾಮಾನ್ಯ ಮನುಷ್ಯನ ಆಹಾರಜ್ಞಾನವನ್ನು ಹಿರಿದುಗೊಳಿಸುತ್ತದೆ. ಸಮತೋಲನ ಆಹಾರದಲ್ಲಿ ಆಸಕ್ತಿಯಿರುವವರಿಗೆ ಇಲ್ಲಿ ವಿಪುಲ ವೈವಿಧ್ಯ ಗೋಚರಿಸಬಹುದು. ಆರೋಗ್ಯ ರಕ್ಷಣೆಗೆ ಮಾತ್ರ ಆಹಾರದ ಬಗ್ಗೆ ತಿಳಿದರೆ ಸಾಲದು; ಅನಾರೋಗ್ಯದಲ್ಲೂ ಯಾವ ಆಹಾರ ಪಥ್ಯ ಮಾಡಬೇಕು ಎನ್ನುವ ಅರಿವು ಎಲ್ಲರಿಗೂ ಅಗತ್ಯ. ಸಾಮಾನ್ಯವಾಗಿ ಕುಟುಂಬದ ಸದಸ್ಯರಿಗೆ ಕಾಯಿಲೆಯಾದಾಗ, ಗೃಹಿಣಿಯಾದವ ಳಿಗೆ ಅವರಿಗೆ ಸೂಕ್ತ ಆಹಾರ ಹೊಂದಿಸುವುದರಲ್ಲಿ ಗಲಿಬಿಲಿ ಉಂಟಾಗುತ್ತದೆ. ಆ ತೊಂದರೆ ನಿವಾರಿಸಲು ಈ ಹೊತ್ತಗೆಯಲ್ಲಿ ಸಾಮಾನ್ಯ ಕಾಯಿಲೆಗಳಲ್ಲಿ ಆಹಾರ-ಪಥ್ಯವನ್ನು ವಿಸ್ತೃತ ಪಟ್ಟಿಗಳೊಂದಿಗೆ ನಮೂದಿಸಲಾಗಿದೆ. ಇದು ಆಹಾರದ ಬಗ್ಗೆ ಕಾಳಜಿಯಿರುವವರಿಗೆಲ್ಲ ಉಪಯುಕ್ತವಾಗುತ್ತದೆ ಎಂಬ ಆಶಯ ನನ್ನದು.
